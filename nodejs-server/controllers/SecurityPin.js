'use strict';

var utils = require('../utils/writer.js');
var SecurityPin = require('../service/SecurityPinService');

module.exports.clientPINDisablePost = function clientPINDisablePost (req, res, next) {
  var body = req.swagger.params['Body'].value;
  var authToken = req.swagger.params['AuthToken'].value;
  var contentType = req.swagger.params['Content-Type'].value;
  SecurityPin.clientPINDisablePost(body,authToken,contentType)
    .then(function (response) {
      utils.writeJson(res, response);
    })
    .catch(function (response) {
      utils.writeJson(res, response);
    });
};

